from random import randint
n=((randint(0,10)),(randint(0,10)),(randint(0,10)),(randint(0,10)),(randint(0,10)),)
print(f"Os números sorteados foram:")
for i in n:
    print(f'{i}',end=' ')
print(f"\nO maior número é {max(n)}")
print(f"O menor número é {min(n)}")

